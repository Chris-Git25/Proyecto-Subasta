package Clases;

/**
 *
 * @author Christian
 */
public class Moneda {
    private int id;
    private String nombre;
    private String denominacion;
    private float total_montaje;
    private String forma;
    private String metal;
    private float diametro;
    private String canto;
    private float peso;
    private String acunacion;
    private String motivo;
    private String anverso;
    private String reverso;

    public Moneda() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDenominacion() {
        return denominacion;
    }

    public void setDenominacion(String denominacion) {
        this.denominacion = denominacion;
    }

    public float getTotal_montaje() {
        return total_montaje;
    }

    public void setTotal_montaje(float total_montaje) {
        this.total_montaje = total_montaje;
    }

    public String getForma() {
        return forma;
    }

    public void setForma(String forma) {
        this.forma = forma;
    }

    public String getMetal() {
        return metal;
    }

    public void setMetal(String metal) {
        this.metal = metal;
    }

    public float getDiametro() {
        return diametro;
    }

    public void setDiametro(float diametro) {
        this.diametro = diametro;
    }

    public String getCanto() {
        return canto;
    }

    public void setCanto(String canto) {
        this.canto = canto;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public String getAcunacion() {
        return acunacion;
    }

    public void setAcunacion(String acunacion) {
        this.acunacion = acunacion;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

    public String getAnverso() {
        return anverso;
    }

    public void setAnverso(String anverso) {
        this.anverso = anverso;
    }

    public String getReverso() {
        return reverso;
    }

    public void setReverso(String reverso) {
        this.reverso = reverso;
    }
    
    
}
